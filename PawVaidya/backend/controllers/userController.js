// Get admin messages for user
export const getUserMessages = async (req, res) => {
    try {
        const { token } = req.headers;
        
        if (!token) {
            return res.json({
                success: false,
                message: 'Not Authorized'
            });
        }
        
        const token_decode = jwt.verify(token, process.env.JWT_SECRET);
        const userId = token_decode.id;
        
        const now = new Date();
        
        // Get all active messages for users
        const messages = await adminMessageModel.find({
            isActive: true,
            $and: [
                {
                    $or: [
                        { targetType: 'all' },
                        { targetType: 'users' },
                        { targetType: 'specific', targetIds: userId }
                    ]
                },
                {
                    $or: [
                        { expiresAt: null },
                        { expiresAt: { $gt: now } }
                    ]
                }
            ]
        }).sort({ priority: -1, createdAt: -1 });
        
        res.json({
            success: true,
            messages
        });
    } catch (error) {
        console.error('Error getting user messages:', error);
        res.json({
            success: false,
            message: error.message
        });
    }
};

// Mark message as read by user
export const markMessageAsRead = async (req, res) => {
    try {
        const { token } = req.headers;
        const { messageId } = req.body;
        
        if (!token) {
            return res.json({
                success: false,
                message: 'Not Authorized'
            });
        }
        
        const token_decode = jwt.verify(token, process.env.JWT_SECRET);
        const userId = token_decode.id;
        
        const message = await adminMessageModel.findById(messageId);
        
        if (!message) {
            return res.json({
                success: false,
                message: 'Message not found'
            });
        }
        
        // Check if already read
        const alreadyRead = message.readBy.some(read => read.userId === userId);
        
        if (!alreadyRead) {
            message.readBy.push({
                userId,
                readAt: new Date()
            });
            await message.save();
        }
        
        res.json({
            success: true,
            message: 'Message marked as read'
        });
    } catch (error) {
        console.error('Error marking message as read:', error);
        res.json({
            success: false,
            message: error.message
        });
    }
};

